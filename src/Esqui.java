class Esqui {

	String nom;
	int altitud;
	int remuntador;
	int quant_pistes;
	int forfait;

	Esqui() {

	}

	Esqui(String nom, int altitud, int remuntador, int quant_pistes, int forfait) {
		this.nom = nom;
		this.altitud = altitud;
		this.remuntador = remuntador;
		this.quant_pistes = quant_pistes;
		this.forfait = forfait;
	}

}
